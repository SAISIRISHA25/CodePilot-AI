"""
Workflow nodes.

Every function in this module is a NODE FACTORY: it accepts an already
constructed application/rag service and returns a plain
``GraphState -> GraphState`` function suitable for
``StateGraph.add_node``. This is the dependency-injection seam for the
entire workflow package - node factories never construct their own
services, and the functions they return contain no business logic of
their own. Each node's body does exactly three things: read fields off
``GraphState``, call exactly one existing service method, and fold the
result back into a new ``GraphState`` via ``model_copy``.

Design decision:
    The seven SDLC agent nodes are generated by one private helper,
    ``_build_agent_node``, rather than seven near-identical copies of
    the same fifteen lines. This is not a violation of "one node per
    workflow step" - each requested node still exists as its own named,
    independently importable factory function - it is simply
    Don't-Repeat-Yourself applied to their shared implementation, which
    is itself a SOLID (Open/Closed) concern: adding an eighth agent node
    in the future means adding one three-line wrapper, not copying and
    re-verifying fifteen lines of dispatch/error-handling logic.

    Node names in this module are aligned to the actual ``AgentType``
    enum values implemented in the Agent Foundation package
    (requirements, architecture, planning, coding, testing,
    documentation, review). No ``deployment_agent_node`` is implemented,
    because no deployment/DevOps agent exists in that package - adding
    one here would mean either fabricating agent behavior inside the
    workflow layer (forbidden: workflows must not implement agent logic)
    or silently mislabeling another agent, both of which were rejected.
    This is called out explicitly rather than papered over.
"""

