def save_output_node(workflow_run_repository: WorkflowRunRepository) -> WorkflowNode:
    """Build the node that finalizes a workflow execution.

    Marks the workflow as completed if it finished with no recorded
    errors, or failed if any step along the way recorded one. This node
    writes the final run status to the database.

    Args:
        workflow_run_repository: SQLite repository to track runs.

    Returns:
        WorkflowNode: A node function that marks the workflow complete
        or failed.
    """

    def _node(state: GraphState) -> GraphState:
        final_phase = WorkflowPhase.FAILED if state.errors else WorkflowPhase.COMPLETED
        summary = (
            f"Workflow failed with {len(state.errors)} error(s)."
            if state.errors
            else f"Finalized workflow with {len(state.generated_outputs)} output(s)."
        )
        
        run_id = state.metadata.get("run_id")
        if run_id:
            workflow_run_repository.update_workflow_run(
                run_id=str(run_id),
                status=final_phase.value,
                current_phase=final_phase.value,
                errors=state.errors,
            )

        return state.model_copy(
            update={
                "current_phase": final_phase,
                "conversation_history": _record_history(state, final_phase, summary),
            }
        )

    return _node